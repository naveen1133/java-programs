
public class swap {

	public static void main(String[] args) {
	
	//swapping of 2 numbers
		int a=10;
		int b=20;
		 System.out.println("Before swapping " + "a=" + a + " " + "and" + " " + "b=" + b );
		
		int temp = a;
		a=b;
		b=temp;
		
		System.out.println("After swapping " + "a=" + a + " " + "and" + " " + "b=" + b );

	}

}
