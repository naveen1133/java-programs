
public class swap2 {

	public static void main(String[] args) {
		// swapping with out temp variable
		
		int a=10;
		int b=20;
		
		System.out.println("Before swapping " + "a=" + a + " " + "and" + " " + "b=" + b );
		
		b=b-a;
		a=a+b;
		
		System.out.println("After swapping " + "a=" + a + " " + "and" + " " + "b=" + b );
		
		

	}

}
